package s.a.asic41;

public class Perhitungan {

    int a;
    int b;


    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = this.b;
    }

    int getPenambahan() {
        return a + b;
    }

}